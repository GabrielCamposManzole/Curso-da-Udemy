let num1 = 5.7;

let num2 = Math.floor(num1); // Arredonda para baixo
console.log(num2 + " é o número arredondado para baixo de " + num1);
let num3 = Math.ceil(num1); // Arredonda para cima
console.log(num3 + " é o número arredondado para cima de " + num1);
let num4 = Math.round(num1); // Arredonda para o inteiro mais próximo
console.log(num4 + " é o número arredondado mais próximo de " + num1);
let num5 = Math.max(1, 2, 3, 4, 5); // Retorna o maior número
console.log(num5 + " é o maior número");
let num6 = Math.min(1, 2, 3, 4, 5); // Retorna o menor número
console.log(num6 + " é o menor número");
const aleatorio = Math.round(Math.random() * (10 - 5) + 5); // Gera um número aleatório entre 0 e 1
console.log(aleatorio + " é um número aleatório entre 5 e 10");
